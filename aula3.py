# Aspas simples 
print('Dennis Augusto')
# Aspas duplas 
print("Dennis Augusto")
# Escape 
print("Dennis \"Augusto")
print("Dennis \"Augusto\"")

print('Dennis "Augusto"')
print("Dennis 'Augusto'")
# r
print(r"Dennis \"Augusto\"") # usado mais para expressões regulares

