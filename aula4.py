# Tipos int e float
"""
 int -> Números inteiros
 O tipo int representa qualquer número
 positivo ou negativo. Int sem sinel é considerado positivo     
"""
print(11) # int
print(-11) # int
print(0) # int

"""
float -> Números com ponto flutuante 
O tipo float representa qualquer número
positivo ou negativo com ponto flutuante
float sem sinel é considerado positivo
"""
print(1.1) #float
print(-1.1) #float
print(0.0) #float
print(1.0, 10.11) #float

# A função type mostra o tipo que o Python inferiu ao valor
print(type('Dennis'))
print(type(11))
print(type(1.1))
print(type(1.1), type(-1.1))